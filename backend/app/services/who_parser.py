# =====================================================
# NORMALIZE DISEASE
# =====================================================

def extract_disease(
    title: str
):

    title_lower = (
        title
        .lower()
        .strip()
    )

    # -------------------------------------------------
    # WORD-BOUNDARY MATCHING
    # -------------------------------------------------

    padded_title = (
        f" {title_lower} "
    )

    for alias, disease in (
        DISEASE_ALIASES.items()
    ):

        padded_alias = (
            f" {alias.lower()} "
        )

        if padded_alias in padded_title:

            return disease

    return "Unknown"

# =====================================================
# EXTRACT COUNTRY
# =====================================================

def extract_country(
    title: str
):

    title_lower = (
        title
        .lower()
        .strip()
    )

    for country_name, iso2 in (
        COUNTRY_MAP.items()
    ):

        if country_name in title_lower:

            return {

                "name":
                    country_name.title(),

                "iso2":
                    iso2,
            }

    return {

        "name":
            "Unknown",

        "iso2":
            None,
    }